//樱桃
$(document).ready(function () {
    $('.cherry').click(function () {
        var hideobj=document.getElementById("hidebg");
        hidebg.style.display="block";
        hidebg.style.height=document.body.clientHeight+"px";
        $('#cherry').fadeIn("slow");
    })
    $('.close').click(function () {
        $('#cherry').fadeOut("slow");
        $('#hidebg').fadeOut("slow");
    })
})
